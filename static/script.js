function getCropDescription(cropName) {
    return CROP_DESCRIPTIONS[cropName] || `${cropName} — культура, яка добре пристосована до ваших умов.`;
}

// Функція отримання шляху до картинки
function getCropImage(cropName) {
    // Перетворюємо назву для файлу
    let imageName = cropName.toLowerCase().replace(/ /g, '_').replace(/'/g, '');
    return `/static/images/${imageName}.jpg`;
}

// Передбачення через сервер
async function predict() {
    const temperature = parseFloat(document.getElementById('temperature').value);
    const humidity = parseFloat(document.getElementById('humidity').value);
    const ph = parseFloat(document.getElementById('ph').value);
    const rainfall = parseFloat(document.getElementById('rainfall').value);
    
    if (isNaN(temperature) || isNaN(humidity) || isNaN(ph) || isNaN(rainfall)) {
        alert("Будь ласка, заповніть всі поля!");
        return;
    }
    
    const resultDiv = document.getElementById('result');
    const resultContent = document.getElementById('resultContent');
    resultContent.innerHTML = '<div class="loading"> Аналіз даних...</div>';
    resultDiv.classList.remove('hidden');
    
    try {
        const response = await fetch('/predict', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ temperature, humidity, ph, rainfall })
        });
        
        const data = await response.json();
        
        if (data.success) {
            const cropName = data.crop;
            const description = getCropDescription(cropName);
            const imagePath = getCropImage(cropName);
            
            resultContent.innerHTML = `
                <div class="crop-card">
                    <img class="crop-image" src="${imagePath}" alt="${cropName}" onerror="this.src='/static/images/default.jpg'">
                    <div class="crop-name">${cropName}</div>
                    <div class="crop-probability"> Впевненість: ${data.confidence}%</div>
                    <div class="crop-description">
                        <p>${description}</p>
                    </div>
                </div>
            `;
        } else {
            resultContent.innerHTML = `<div style="color: red;"> Помилка: ${data.error}</div>`;
        }
    } catch (error) {
        resultContent.innerHTML = `<div style="color: red;"> Помилка: ${error.message}</div>`;
    }
}

// Ініціалізація
document.addEventListener('DOMContentLoaded', function() {
    console.log("🌱 AgroRadar готовий");
    document.getElementById('predictBtn').addEventListener('click', predict);
    
    document.querySelectorAll('input').forEach(input => {
        input.addEventListener('keypress', (e) => {
            if (e.key === 'Enter') predict();
        });
    });
});
